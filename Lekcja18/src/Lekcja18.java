public class Lekcja18 {
    public static void main(String[] args){
        //Math klasa wbudowana w j. java
        int a = 5;
        int b = 2;
        int c = -2;
        System.out.println(Math.max(a, b)); //max wyciaga najwieksza wartosc
        System.out.println(Math.min(a, c)); //wyciaga najmniejsza wartosc
        System.out.println(Math.abs(c)); //wartosc bezwzgledna absolute
        System.out.println(Math.pow(b, 5)); //patega! b podstawa
        System.out.println(Math.sqrt(16)); //pierwiastek
        System.out.println(Math.round(2.4)); //zaokraglanie
        System.out.println(Math.ceil(2.4)); //zaokraglanie do góry ceil-sufit
        System.out.println(Math.floor(2.4)); //zaokraglanie do dpłu floor-podloga
        System.out.println(Math.PI); //Pi
        System.out.println(Math.E); //Pi

    }
}
